    private void orderMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderMenuActionPerformed
         // Show confirmation dialog
    int result = JOptionPane.showConfirmDialog(this, "정말 주문하시겠습니까?", "주문 확인", JOptionPane.YES_NO_OPTION);

    // Check the user's choice
    if (result == JOptionPane.YES_OPTION) {
        // User clicked "Yes", perform order-related actions here
        // 예를 들어 주문 내역에 추가하는 등의 동작을 수행할 수 있습니다.
        JOptionPane.showMessageDialog(this, "주문이 완료되었습니다. 감사합니다!", "주문 완료", JOptionPane.INFORMATION_MESSAGE);
        toOrderingFrame(); 
        //주문이 완료되면 필드 초기화
        resetOrderDetails();
    } else {
        // User clicked "No" or closed the dialog
        JOptionPane.showMessageDialog(this, "주문이 취소되었습니다.", "주문 취소", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_orderMenuActionPerformed

    
