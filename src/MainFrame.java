   private void addMenuToSelectedMenu(String menuNameAndPrice) {
    // Extract the menu name before the space
    String menuName = menuNameAndPrice.substring(0, menuNameAndPrice.indexOf(' '));

    String currentText = selectedMenu.getText();
    if (currentText.isEmpty()) {
        selectedMenu.setText(menuName);
    } else {
        selectedMenu.setText(currentText + ", " + menuName);
    }
}
   
   private void addPriceToTotalPay(String menuNameAndPrice){
        // Extract the price after the space (remove non-numeric characters)
    String priceString = menuNameAndPrice.replaceAll("[^0-9]", "");

    // Check if the priceString is not empty after removing non-numeric characters
    if (!priceString.isEmpty()) {
        try {
            int price = Integer.parseInt(priceString);

            // Add the price to totalPay
            int currentTotalPay = Integer.parseInt(totalPay.getText());
            int newTotalPay = currentTotalPay + price;
            totalPay.setText(Integer.toString(newTotalPay) );
        } catch (NumberFormatException e) {
            // Handle the exception (print or log the error, or take appropriate action)
            System.err.println("Error parsing price: " + e.getMessage());
        }
    } else {
        // Handle the case where priceString is empty
        System.err.println("Empty price string");
    }


   }
   
   private void addMenuItem(JPanel panel, String menuName, String price, String imagePath) {
    // Create a panel for each menu item
    JPanel menuItemPanel = new JPanel(new BorderLayout());

    // Add menu image to the panel
    ImageIcon icon = new ImageIcon(getClass().getResource(imagePath));
    JLabel menuImage = new JLabel(new ImageIcon(icon.getImage().getScaledInstance(90, 90, Image.SCALE_DEFAULT)));
    menuItemPanel.add(menuImage, BorderLayout.WEST);

    // Add menu name and price to the panel
    JLabel menuLabel = new JLabel(menuName + " " + price);
    menuItemPanel.add(menuLabel, BorderLayout.CENTER);

    // Add the menu item panel to the main panel
    panel.add(menuItemPanel);
}
     
